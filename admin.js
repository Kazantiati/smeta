module.exports = async function handler(req, res) {
  res.setHeader('Access-Control-Allow-Origin', '*');
  res.setHeader('Access-Control-Allow-Methods', 'POST, OPTIONS');
  res.setHeader('Access-Control-Allow-Headers', 'Content-Type, Authorization');
  if (req.method === 'OPTIONS') return res.status(200).end();
  if (req.method !== 'POST') return res.status(405).end();

  const token = req.headers.authorization?.replace('Bearer ', '');
  if (!token) return res.status(401).json({ error: 'Не авторизован' });

  const supabase = createClient(
    process.env.SUPABASE_URL,
    process.env.SUPABASE_SERVICE_KEY
  );

  const { data: { user }, error: authError } = await supabase.auth.getUser(token);
  if (authError || !user) return res.status(401).json({ error: 'Не авторизован' });

  const { messages, estimateId, title } = req.body;
  if (!messages || !Array.isArray(messages)) {
    return res.status(400).json({ error: 'Неверный формат сообщений' });
  }

  try {
    const anthropic = new Anthropic({ apiKey: process.env.ANTHROPIC_API_KEY });

    const response = await anthropic.messages.create({
      model: 'claude-sonnet-4-20250514',
      max_tokens: 8192,
      system: SYSTEM_PROMPT,
      messages: messages
    });

    const assistantText = response.content[0].text;
    const updatedMessages = [...messages, { role: 'assistant', content: assistantText }];

    // Auto-generate title from first user message
    const autoTitle = title || (() => {
      const firstMsg = messages[0]?.content || '';
      const t = firstMsg.slice(0, 60).replace(/\n/g, ' ').trim();
      return t || 'Новая смета';
    })();

    let savedId = estimateId;
    if (estimateId) {
      await supabase.from('estimates')
        .update({ messages: updatedMessages, updated_at: new Date().toISOString() })
        .eq('id', estimateId)
        .eq('user_id', user.id);
    } else {
      const { data: newEst } = await supabase.from('estimates')
        .insert({ user_id: user.id, title: autoTitle, messages: updatedMessages })
        .select()
        .single();
      savedId = newEst?.id;
    }

    res.json({ message: assistantText, estimateId: savedId });
  } catch (err) {
    console.error('Chat error:', err);
    res.status(500).json({ error: 'Ошибка: ' + err.message });
  }
};
